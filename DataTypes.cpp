#include<iostream>
using namespace std;
int main(){
    int a = 10;
    cout<<"Size of int is "<<sizeof(a)<<endl;

    float b = 3.14f;
    cout<<"Size of float is "<<sizeof(b)<<endl;

    char c = '$';
    cout<<"Size of Char is "<<sizeof(c)<<endl;

    double d = 44.484784774878748884848478;
    cout<<"Size of double is "<<sizeof(d)<<endl;

    bool e = 1;
    cout<<"Size of boolean is "<<sizeof(e)<<endl;

    short int f=23;
    cout<<"Size of short is "<<sizeof(f)<<endl;

    long int g;
    cout<<"Size of long is "<<sizeof(g)<<endl;
}